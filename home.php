<h3> Um pouco sobre mim... </h3>
	<p>
	Meu interesse por informática começou em 1989 quando tive a oportunidade
	de fazer meu primeiro curso de informática, nesse curso aprendi a 
	programar em linguagem BASIC usando computadores MSX, poucas pessoas tinha
	acesso ao computador porque era muito caro. Em 1993 com 
	ajuda do meu pai comprei meu primeiro computador um ITAUTEC 486DX2 66MHz
	nesse PC inicialmente claro o maior uso era jogar e fazer os trabalhos da
	escola e imprimia na impressora matricial. Começando a fuçar não demorou
	muito tempo para ferrar o PC, mexendo nos arquivos para arrumar o som
	para um joguinho apaguei uma linha no AUTOEXEC.BAT e o Windows 3.1 
	"não subia" fiquei deseperado foi quando um amigo do meu irmão veio
	sentou em poucos minutos consertou colocando a linha do himem.sys, 
	esnobou a gente, fiquei curioso comecei a estudar sobre os computadores,
	logo aprendi a formatar o computador, dava um probleminha juntava
	aquele monte de disquetes e passava o dia formatando o meu computador.
	Logo aprendi instalar jogos e programas, a maior alegria foi quando
	instalei o jogo Indycar e descobri que tinha um arquivo com o nome
	dos pilotos e comecei mudar o nome de todos e personalizei meu primeiro
	jogo. Em 1994 assinei o contrato com primeiro provedor de internet que
	ficava na Penha/SP, fiz minha primeira conexão, logo começou uma nova DR 
	com meus pais por causa do telefone, não tinha muito o que navegar 
	usava o Cadê para buscar páginas, um amigo do meu pai que trabalhava
	no despachante fazia consulta de veículos usando a internet e me deu 
	um programinha que chamava VideoTexto e comecei a tirar extrato e saldo
	da conta do meu pai pela internet e não precisava mais ficar indo ao
	banco só para tirar saldo para ele. Em 1995 o provedor que acessava 
	internet deixou de operar e assinei novo contrato com a Universo Online
	que começava a operar naquele ano. Nesse período estava terminei o curso
	de Técnico em Eletrônica, pois um sonho de infância aprender e desde
	pequeno tinha sonho de ser Engenheiro, fica observando o tecnico 
	arrumando as máquinas de fliperama, essa é uma história para outro
	momento. Voltando para informática em 1997 fiz primeiros cursos de 
	Hardware e Redes em uma escola no centro de Guarulhos, aprendi muito, 
	precisava aprender mais, fiz minha primeira certificação em Hardware
	(Dominio Tecnologia) Manutenção Total de Micros PC. No final do curso
	montei minha empresa informal de venda de computadores, montei meu 
	portfólio de peças e comecei montar e vender para o pessoal da empresa
	que trabalhava. No mesmo ano ainda comecei a trabalhar no Colégio 
	Albert Einstein como técnico de laboratório, em 1998 tive a oportunidade
	de lecionar no lugar de um outro professor disciplinas de informática
	para técnicos em eletrônica, ministrei também disciplinas de hardware, 
	logo depois fiz cursos de Desenho usando AutoCAD 2D e 3D e passei a 
	ministrar aulas de desenho usando o AutoCAD. Nesse tempo continuava a
	estudar informática fazendo cursos de redes de computadores foi quando
	conheci os sistemas de Rede Novell, Windows NT e meus primeiros
	contatos com o sistema Linux.
</p>
