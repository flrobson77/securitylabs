<h1> Security Labs </h1>

<p>
O SecurityLabs é um projeto que tem objetivo de estudar, aprender, pesquisar e apresentar técnicas para melhorar a configuração de serviços de rede, servidores e infraestrutrura de rede para atender o mundo do Devops.
A ideia é discutir a mudança do perfil da carreira do sysadmin (Administrador de Sistemas) sobre a infraestrutrura como código para acompanhar o desenvolvimento.
Atualmente a cultura Devops faz surgir novos desafios que é a união de todos os times (desenvolvimentos, operações e segurança) cuidando do projeto desde o inicio.
O objetivo do SecurityLabs é criar laboratórios, testes de configuração, divulgação de boas práticas de Segurança da Informação com o objetivo de aumentar a proteção frentes as mais diversas ameaças (malwares, ataques DDoS, Varredura, Uso de Scanner's, etc.), com isso diminuindo risco da vulnerabilidade.
</p>
