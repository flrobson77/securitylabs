<h2> Apresentação </h2>
<p>
O SecurityLabs é um projeto pessoal para compartilhar conhecimento
de segurança da informação da parte defensiva, a ideia é apresentar
aqui técnicas para melhorar a configuração de serviços de rede, servidores
e infraestrutrura de rede para aumentar a segurança, dificultar o acesso
indevido.
</p> 
<p>
O canal do Securitylabs pretende ainda discutir a mudança do perfil da 
carreira do sysadmin (Administrador de Sistemas) sobre a infraestrutrura
como código e os aspectos relevantes que podem impactar a segurança da
informação.</p>
</section>

<section class="secao">
<div class="section3" id="objetivo">
<h2> Objetivo </h2>
<p>
Com SecurityLabs pretende-se publicar análise dos experimentos sobre as 
principais ameaças (malwares, ataques, DDoS, Varredura, Uso de Scanner's, 
etc.) sobre as vulnerabilidades apresentadas.
</p>
<p>
Realizar testes e configuração de ferramentas de proteção como firewall, 
sistemas de detecção de intrusão e ferramentas de monitoramento.
</p>
<p>
Promover a aplicação das ferramentas Devops, operação na nuvem, bem como
na implementação de infraestrutura como código criando ambientes rápidos
com alta escalabilidade e segurança.
</p>
</section>

